require 'spec_helper'

describe TreatmentArm do

  it { is_expected.to have_fields(:name, :version, :description, :target_id, :target_name, :gene,
                                  :treatment_arm_status).of_type(Object) }
  it { is_expected.to have_fields(:num_patients_assigned, :max_patients_allowed).of_type(Integer) }
  it { is_expected.to have_field(:statusLog).of_type(Hash) }
  it { is_expected.to have_field(:date_created).of_type(DateTime)}
  it { is_expected.to embed_many(:treatment_arm_drugs).of_type(Drug) }
  it { is_expected.to embed_many(:exclusion_diseases).of_type(Disease) }
  it { is_expected.to embed_many(:exclusion_drugs).of_type(Drug) }
  it { is_expected.to embed_many(:exclusion_criterias).of_type(ExclusionCriteria) }
  it { is_expected.to embed_many(:pten_results).of_type(PtenResult) }
  it { is_expected.to embed_one(:variant_report).of_type(VariantReport)}


end